    private void bookSaveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookSaveBtnActionPerformed
        // TODO add your handling code here:
        try {
            Book b = new Book();
            b.setBookId(bookIdTxt.getText());
            b.setTitle(bookTitleTxt.getText());
            b.setPublishingHouse((bookPubHouseTXt.getText()));
            b.setDateOfPublication(bookRegDate.getDate());
            b.setAuthor(bookAuthorTxt.getText());
            b.setPages(Integer.parseInt(bookPagesTxt.getText()));
            
            new BookController().register(b);
            view();
            JOptionPane.showMessageDialog(rootPane, "Book Registered");
        } catch (Exception e) {
            e.printStackTrace();
}
    }//GEN-LAST:event_bookSaveBtnActionPerformed

