    public void view(){
        List<Book> list = new BookController().findAll();
        String[] title = {"Book_ID","Title","PUBLSHING HOUSE","DATE OF PUBLICATION","AUTHOR","PAGES"};
        Object[][] data = new Object[list.size()][title.length];
        int i = 0;
        
        for(Book b: list){
            data[i][0] = b.getBookId();
            data[i][1] = b.getTitle();
            data[i][2] = b.getPublishingHouse();
            data[i][3] = b.getDateOfPublication();
            data[i][4] = b.getAuthor();
            data[i][5] = b.getPages();
            i++;
        }
        
        bookTable.setModel(new DefaultTableModel(data, title));
    }

